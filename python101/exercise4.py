day = int(input('Day (0-6)? '))

if day == 1 :
    print("Monday")
elif day == 2 :
    print("Tuesday")
elif day == 3 :
    print("Wednesday")
elif day == 4 :
    print("Thursday")
elif day == 5 :
    print("Friday")
elif day == 6 :
    print("Saturday")
elif day == 0 :
    print("Sunday")
else :
    print("Out of range. please put between 0 and 6")
